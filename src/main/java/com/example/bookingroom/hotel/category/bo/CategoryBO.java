package com.example.bookingroom.hotel.category.bo;

import javax.persistence.*;

@Entity(name = "dbo_category")
public class CategoryBO {
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Id
    @Column(name = "id")
    private int id;

    @Column(name = "name")
    private String name;

    public CategoryBO() {
    }

    public CategoryBO(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
